#include "tuple.h"

double	tuple_dot(t_tuple *t1, t_tuple *t2)
{
	double	dot;

	dot = t1->x * t2->x + t1->y * t2->y + t1->z * t2->z + t1->w * t2->w;
	return (dot);
}
